export interface ProxyPayPaymentChannel extends PaymentChannel {}

/** @deprecated Use {@link PaymentRequestSnapshot} instead. */
export interface ProxyPayRequest extends PaymentRequestSnapshot {}

/** @deprecated Use {@link PaymentStatusResponse} instead. */
export interface ProxyPayPaymentStatusResponse extends PaymentStatusResponse {}

// ---------------------------------------------------------------------------
// Find payment response
// ---------------------------------------------------------------------------

/**
 * Generic payment object returned when finding a payment by reference.
 *
 * Note: The exact shape may vary depending on the provider and payment state.
 */
export interface FindPaymentResponse {
  /** Unique transaction identifier. */
  transactionUid?: string;
  /** Current status of the payment. */
  status?: PaymentStatus;
  /** Order details. */
  order?: Order;
  /** Customer details. */
  customer?: Customer;
  /** Payment channel details, if already submitted. */
  paymentChannel?: PaymentChannel;
  /** Error message, if any. */
  error?: string;
  /** Additional provider-specific metadata. */
  metadata?: Record<string, unknown>;
  [key: string]: unknown;
}
